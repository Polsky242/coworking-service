package ru.polskiy.model.type;

/**
 * Enum representing different roles that users can have within the application.
 * Roles determine the level of access and permissions granted to users.
 */
public enum Role {

    /**
     * Represents an administrative role with elevated privileges.
     */
    ADMIN,

    /**
     * Represents a standard user role with basic privileges.
     */
    USER
}

